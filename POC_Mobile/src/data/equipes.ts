/**
 * Retorna prefixos de equipes filtrados por base e opcionalmente por gerência.
 * Inclui sempre as equipes GERE (financeiro/suporte) em qualquer filtro de gerência operacional.
 */
export function equipesPorBase(base: string, gerencia?: "GOMAN" | "GSTC"): string[] {
  return EQUIPES
    .filter((e) => e.base === base && (!gerencia || e.gerencia === gerencia || e.gerencia === "GERE"))
    .map((e) => e.prefixo)
    .sort();
}
